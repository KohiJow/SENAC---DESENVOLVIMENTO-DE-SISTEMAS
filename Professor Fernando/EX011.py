#Utilizando a classe type, para converter valores, exemplos:
#Este exemplo retorna a afirmação do interpretador do Python que é 1 dado do tipo INTEIRO
print('\n')#--> Função utilizada para "pular" linha no python
print('O tipo de dado interpretado pelo Python é:', int('1')+1)

'''
Obs. tudo que está dentro de aspas simples ou dupla é uma STRING, no exemplo a seguir, o interpretador do 
python vai receber uma STRING em formado de número (1), vai converter em um dado
do tipo INTEIRO e vai finalizar o código fazendo a soma da conversão dos valores:
'''
print('O resultado da soma da conversão é',int('1')+1)
print('O resultado da soma da conversão é:',float('1')+1)
print('O resultado da soma da conversão é:',bool('1')!=1)
print('O resultado da soma da conversão é:',str('20')+ '23')
#--> Repare que os espaços são inseridos AUTOMATICAMENTE pelo interpretador do python

