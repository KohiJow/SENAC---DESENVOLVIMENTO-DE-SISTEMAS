'''
Veja que exemplo bacana, os caracteres <. ^ e > representam os eixos de formatação de espaçoes, função nativa da linguagem python,
aplicada a formatação de Strings, repare na saída deste algoritmo, utilizando a função "format", faça
modificações no algoritmo e testes outras possibilidades de formatação
'''

print('{0:<8} | {1:^8} | {2:>8}'.format('Ano', 'Ano', 'Ano'))
