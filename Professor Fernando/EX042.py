'''
Verificando a quantidade de itens de uma lista com o método: len

Exemplo 2
'''
qtd_titulos = ['4', '3', '2', '1']
titulos = qtd_titulos #A variavel titulos RECEBE os valores da lista qtd_titulos

#Uma boa prática é utilizar o método LEN dentro do print, conforme a seguir e 
# configura o resultado

print('\n' + 'A quantidade títulos mundiais do PALMEIRAS são: {}'.format(len(titulos[0:0])), '-> títulos mundiais.' + '\n')
