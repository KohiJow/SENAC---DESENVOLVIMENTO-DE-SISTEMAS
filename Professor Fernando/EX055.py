#Exemplo 2º - Dicionarios e Listas no Python

#Isso e uma LISTA, ok?
frutas = ["maçã", "banana", "laranja"]
print (frutas[1]) # Acessa o segundo elemento: "banana"

#Isso e uma DICIONARIO, ok?

frutas_precos = {"maçã": 2.5, "banana": 1.5, "laranja": 3.0}
print(frutas_precos["banana"]) # Acessa o preço da "banana": 1.5
