# Tipos de dados no Python, mais conhecidos como tipos "Primitivos"
# int -> Tipo inteiro, exemplos:
print(11)
print(-11)
#
# float -> Número que contém casa decimal em QUALQUER linguagem de programação, exemplos:
print(1.5, 15.00)
print(15.00) #Repare que imprimimos dois valores dentro da mesma função "print" separado por ","
#
# str (String) -> é tudo que irá retornar com texto "Mensagens", exemplos:
print("Meu nome é Jow") # Altere seu nome
print('Meu nome é Jow') # Os dois exemplos estão corretos
#
# bool (Boleano) -> retorna verdadeiro (true) ou falso (false), exemplos:
print(10<9) #9 é menor que 10, o retorno é "False"
print(10>9) #10 é maior que 9, o retorno é "True"
print("Eu tenho mais de 18 anos e posso beber?", 19 > 18, "Ok, eu vou beber :-)")
print("Eu tenho menos de 18 anos e posso beber?", 17 < 18, "Ok, e NÃO vou beber :-(")
